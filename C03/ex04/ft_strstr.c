char	*ft_strstr(char *str, char *to_find)
{
	int	i;
	int	j;

	while (str[i] != '\0')
	{
		if (to_find[j] == str[i + j])
			j++;
		if (to_find = '\0')
			return (str[i - j]);
		i++;
	}
}
